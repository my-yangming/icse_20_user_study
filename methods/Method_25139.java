@Nullable private static String createLinkUrl(BugPattern pattern){
switch (pattern.linkType()) {
case AUTOGENERATED:
    return String.format(URL_FORMAT,pattern.name());
case CUSTOM:
  if (pattern.link().isEmpty()) {
    throw new IllegalStateException("If linkType element of @BugPattern is CUSTOM, " + "a link element must also be provided.");
  }
return pattern.link();
case NONE:
return null;
}
throw new AssertionError("Unexpected value for linkType element of @BugPattern: " + pattern.linkType());
}
