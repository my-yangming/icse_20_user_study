public boolean invoke(ActionContext ac){
  ActionChain chain=getActionChain(ac);
  if (chain == null) {
    if (log.isDebugEnabled())     log.debugf("Not chain for req (path=%s, method=%s)",ac.getPath(),ac.getRequest().getMethod());
    return false;
  }
  chain.doChain(ac);
  return ac.getBoolean(ActionContext.AC_DONE,true);
}
