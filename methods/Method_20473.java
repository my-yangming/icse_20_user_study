/** 
 * Helper method for setting the values of a tile matrix array.
 */
private void setMatrixArray(float[] array,float f0,float f1,float f2,float f3,float f4,float f5,float f6,float f7){
  array[0]=f0;
  array[1]=f1;
  array[2]=f2;
  array[3]=f3;
  array[4]=f4;
  array[5]=f5;
  array[6]=f6;
  array[7]=f7;
}
