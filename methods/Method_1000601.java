public static StringBuilder concat(Object c,int[] vals){
  StringBuilder sb=new StringBuilder();
  if (null == vals || 0 == vals.length)   return sb;
  sb.append(vals[0]);
  for (int i=1; i < vals.length; i++)   sb.append(c).append(vals[i]);
  return sb;
}
